package MyBedroom;

public class Ceiling {
	
	private int height;
	private String paintColor;
	
	public Ceiling(int height, String paintColor) {
		this.height = height;
		this.paintColor = paintColor;
	}

	public int getHeight() {
		return height;
	}


	public String getPaintColor() {
		return paintColor;
	}
}
